package algorithms;

/**
 * AddTwoNumbers class contains one method that allows to find the addition between two integer numbers.
 * NOTE: only int parameters are considered.
 */
public class AddTwoNumbers {

    /**
     * Allows to calculate the sum between two numbers.
     *
     * @param param1 corresponds to the first number to be added.
     * @param param2 corresponds to the second number to be added.
     * @return the result of the addition between param1 and param2.
     */
    public int add(int param1, int param2) {
        return param1 + param2;
    }

}


